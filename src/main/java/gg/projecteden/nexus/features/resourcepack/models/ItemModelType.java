	private final Material material;
	private final String model;

	ItemModelType(String model) {
		this(Material.PAPER, model);
	}

	ItemModelType(String model, boolean dyeable) {
		this(dyeable ? Material.LEATHER_HORSE_ARMOR : Material.PAPER, model);
	}

	ItemModelType(String model, Material material) {
		this(material, model);
	}

	public static ItemModelType of(ItemStack item) {
		if (Nullables.isNullOrAir(item))
			return null;

		return of(new ItemBuilder(item));
	}

	public static ItemModelType of(ItemBuilder item) {
		if (Nullables.isNullOrAir(item))
			return null;

		for (ItemModelType itemModelType : values())
			if (itemModelType.getMaterial() == item.material())
				if (Objects.equals(itemModelType.getModel(), item.model()))
					return itemModelType;

		return null;
	}
	
	public static ItemModelType of(String model) {
		for (ItemModelType value : values()) {
			if (value.getModel().equals(model))
				return value;
		}
		return null;
	}

	public boolean is(@Nullable ItemStack item) {
		if (Nullables.isNullOrAir(item))
			return false;

		return item.getType() == material && model.equals(new ItemBuilder(item).model());
	}

	public boolean is(@Nullable DecorationConfig config) {
		if (config == null)
			return false;

		return config.getMaterial() == material && config.getModel() == model;
	}

	public ItemModelInstance getCustomModel() {
		return ItemModelInstance.of(this);
	}

	public boolean canBePlaced() {
		return switch (this) {
			case INFINITE_WATER_BUCKET -> true;
			default -> false;
		};
	}

	@SneakyThrows
	public Field getField() {
		return getClass().getField(name());
	}

	public boolean hasCustomModelData() {
		return getField().isAnnotationPresent(HasCustomModelData.class);
	}

	public boolean isAllowedInVanillaRecipes() {
		return getField().isAnnotationPresent(AllowInVanillaRecipes.class);
	}

	public ItemBuilder getItemBuilder() {
		return new ItemBuilder(this);
	}

	public ItemStack getItem() {
		return getItemBuilder().build();
	}

	public ItemBuilder getNoNamedItem() {
		return getItemBuilder().name("").itemFlags(ItemFlag.HIDE_DYE);
	}

	public ItemBuilder getNamedItemBuilder() {
		return getItemBuilder().name(StringUtils.camelCase(this));
	}

	public ItemStack getNamedItem() {
		return getNamedItemBuilder().build();
	}

	public boolean isInInventoryOf(Player player) {
		for (ItemStack content : player.getInventory().getContents()) {
			if (isNullOrAir(content))
				continue;

			if (this == ItemModelType.of(content))
				return true;
		}

		return false;
	}
